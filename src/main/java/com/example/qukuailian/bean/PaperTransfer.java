package com.example.qukuailian.bean;

public class PaperTransfer {

    String paperNumber;

    String userSk;

    String encryptPrice;

    String algType;

    String encryptStatus;

    String type;

    public String getPaperNumber() {
        return paperNumber;
    }

    public void setPaperNumber(String paperNumber) {
        this.paperNumber = paperNumber;
    }

    public String getUserSk() {
        return userSk;
    }

    public void setUserSk(String userSk) {
        this.userSk = userSk;
    }

    public String getEncryptPrice() {
        return encryptPrice;
    }

    public void setEncryptPrice(String encryptPrice) {
        this.encryptPrice = encryptPrice;
    }

    public String getAlgType() {
        return algType;
    }

    public void setAlgType(String algType) {
        this.algType = algType;
    }

    public String getEncryptStatus() {
        return encryptStatus;
    }

    public void setEncryptStatus(String encryptStatus) {
        this.encryptStatus = encryptStatus;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
